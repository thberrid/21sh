#include "libft.h"

int		ft_atoi(const char *nptr)
{
	int		i;
	int		sign;
	long	rvalue;

	i = 0;
	sign = 1;
	rvalue = 0;
	while ((nptr[i] >= 7 && nptr[i] <= 13) || nptr[i] == ' ')
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign = -1;
		i++;
	}
	while (nptr[i] >= 48 && nptr[i] <= 57)
	{
		rvalue = rvalue * 10 + nptr[i] - 48;
		i++;
	}
	return (rvalue * sign);
}
